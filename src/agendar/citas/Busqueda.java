    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed

        Date date1 = entrada1.getDate();

        long d1 = date1.getTime();

        java.sql.Date Entrada1 = new java.sql.Date(d1);

        Date date2 = salida1.getDate();

        long d2 = date2.getTime();

        java.sql.Date Salida1 = new java.sql.Date(d2);

        Connection con = null;

        try {

            con = getConection();

            ps = con.prepareStatement("UPDATE Contacto SET Nombre=?, Telefono=?, Correo=?, Marca=?, Modelo=?, Entrada=?, Salida=? WHERE Cc=? ");

            ps.setString(1, txtnombre1.getText());
            ps.setString(2, txttelefono1.getText());
            ps.setString(3, txtcorreo1.getText());
            ps.setString(4, txtmarca1.getText());
            ps.setString(5, txtmodelo1.getText());
            ps.setDate(6, Entrada1);
            ps.setDate(7, Salida1);
            ps.setString(8, txtcc1.getText());

            int res = ps.executeUpdate();

            if (res > 0) {

                JOptionPane.showMessageDialog(null, "Persona modificada con exito");

            } else {

                JOptionPane.showMessageDialog(null, "Error al modificar");
            }

            con.close();

        } catch (Exception e) {

            System.err.println(e);
        }

    }//GEN-LAST:event_btnsaveActionPerformed

