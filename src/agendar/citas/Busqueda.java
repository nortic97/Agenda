    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed

        Connection con = null;

        try {

            con = getConection();
            ps = con.prepareStatement("SELECT * FROM Contacto WHERE Cc = ?");
            ps.setString(1, txtcc1.getText());

            rs = ps.executeQuery();

            if (rs.next()) {

                txtnombre1.setText(rs.getString("Nombre"));
                txttelefono1.setText(rs.getString("Telefono"));
                txtcorreo1.setText(rs.getString("Correo"));
                txtmarca1.setText(rs.getString("Marca"));
                txtmodelo1.setText(rs.getString("Modelo"));

                SimpleDateFormat formatoFecha = new SimpleDateFormat("yyyy-MM-dd");
                Date FechaTxt;

                FechaTxt = formatoFecha.parse(rs.getString("Entrada"));
                entrada1.setDate(FechaTxt);

                SimpleDateFormat formatoFecha1 = new SimpleDateFormat("yyyy-MM-dd");
                Date FechaTxt1;


                FechaTxt1 = formatoFecha1.parse(rs.getString("Salida"));
                salida1.setDate(FechaTxt1);

              

            } else {
                JOptionPane.showMessageDialog(null, "No hay registro");
            }

        } catch (Exception e) {

            System.err.println(e);

        }
    }//GEN-LAST:event_btnbuscarActionPerformed

